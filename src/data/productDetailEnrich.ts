function hashU32(s: string): number {
  let h = 2166136261
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i)
    h = Math.imul(h, 16777619)
  }
  return h >>> 0
}

function pick<T>(s: string, options: T[]): T {
  return options[hashU32(s) % options.length]
}

function row(key: string, value: string): { key: string; value: string } {
  return { key, value }
}

/** Разбивает longDescription на блоки по маркеру «## заголовок» в начале строки. */
export function parseDescriptionSections(text: string): { title: string | null; body: string }[] {
  const t = text.trim()
  if (!t) return []
  const chunks = t.split(/\n##\s+/)
  if (chunks.length === 1) return [{ title: null, body: chunks[0].trim() }]
  const out: { title: string | null; body: string }[] = []
  const first = chunks[0].trim()
  if (first) out.push({ title: null, body: first })
  for (let i = 1; i < chunks.length; i++) {
    const chunk = chunks[i]
    const nl = chunk.indexOf('\n')
    const title = (nl === -1 ? chunk : chunk.slice(0, nl)).trim()
    const body = (nl === -1 ? '' : chunk.slice(nl + 1)).trim()
    if (title || body) out.push({ title: title || null, body })
  }
  return out
}

